# what is comprehensions? 
# Comprehensions are a concise way to create lists, sets, or dictionaries in Python.
# They provide a syntactic shortcut for generating these collections from iterables.

# where to use comprehensions?
# Comprehensions are useful when you want to create a new collection by applying an expression to each item in an existing iterable,

# real life example :
# for filtering data , transforming data , creating combinations of data etc. 

# what is the purpose :
# cleaner code , better performance , improved readability

# types of comprehensions:
# list , set , dictionary , generator

# generator is not a data type it is a collection of items that can be iterated over one at a time and does not store all the items in memory at once. 